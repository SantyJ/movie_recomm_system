# =============================================================================
# SVD COMPUTATION  (cached — recomputes only when inputs change)
# =============================================================================

@st.cache_data(show_spinner=False)
def compute_svd(ratings_df, poison=False, target_movie_id=None, victim_id=None):
    """
    Runs SVD Matrix Factorization on the ratings DataFrame and returns a
    fully-dense predicted rating matrix.

    This is the same algorithm as my_svd_approach.py, adapted for live
    interactive use. @st.cache_data means Streamlit only recomputes when
    the input arguments actually change — selecting a different user does
    NOT trigger a recompute if the SVD inputs are unchanged.

    Args:
        ratings_df      (DataFrame): Training ratings (and injected bots if poisoned).
        poison          (bool)     : Whether to inject shilling attack bots.
        target_movie_id (int|None) : Movie ID the attack is trying to promote.
        victim_id       (int|None) : User ID being targeted by the attack.

    Returns:
        preds_df (DataFrame): Dense (users × movies) predicted rating matrix.
    """
    df_copy = ratings_df.copy()

    # -------------------------------------------------------------------------
    # OPTIONAL E: ROBUSTNESS — Shilling Attack Injection
    # -------------------------------------------------------------------------
    # If attack mode is active, inject 500 synthetic bot users into the
    # rating matrix before running SVD. Each bot:
    #   1. Perfectly clones the victim user's entire rating history
    #      (user-aligned attack — makes bots appear maximally similar to victim)
    #   2. Gives the target "bad" movie a 5-star rating
    #
    # Because SVD's latent factor decomposition picks up on user similarity,
    # the bot cluster's collective 5-star signal gets amplified into the
    # victim's latent vector, pushing the target movie up their ranked list.
    # This demonstrates a real-world vulnerability in CF-based systems.
    if poison and target_movie_id and victim_id:
        victim_rows = df_copy[df_copy['userId'] == victim_id].copy()

        bot_list = []
        for bot_id in range(10000, 10500):   # 500 bot user IDs
            # Clone the victim's full rating history under the bot's ID
            bot_user           = victim_rows.copy()
            bot_user['userId'] = bot_id
            bot_list.append(bot_user)

            # Add the bot's 5-star rating for the attack target movie
            bot_list.append(pd.DataFrame([{
                'userId':  bot_id,
                'movieId': target_movie_id,
                'rating':  5.0
            }]))

        bots_df = pd.concat(bot_list, ignore_index=True)
        df_copy = pd.concat([df_copy, bots_df], ignore_index=True)

    # -------------------------------------------------------------------------
    # SVD Pipeline (identical to my_svd_approach.py)
    # -------------------------------------------------------------------------

    # Build User-Item pivot matrix
    R_df   = df_copy.pivot(index='userId', columns='movieId', values='rating')
    users  = R_df.index.tolist()
    movies = R_df.columns.tolist()

    # Mean-center per user to remove individual rating bias, fill NaN with 0
    user_ratings_mean = R_df.mean(axis=1).values
    R_demeaned        = R_df.sub(user_ratings_mean, axis=0).fillna(0).values

    # Truncated SVD: decompose into k=50 latent factors
    # R_demeaned ≈ U · Σ · Vᵀ
    U, sigma, Vt = svds(R_demeaned, k=50)
    sigma        = np.diag(sigma)   # convert 1D singular values to diagonal matrix

    # Reconstruct dense predicted ratings and add user means back
    all_user_predicted_ratings = (
        np.dot(np.dot(U, sigma), Vt)
        + user_ratings_mean.reshape(-1, 1)
    )
    preds_df = pd.DataFrame(all_user_predicted_ratings, index=users, columns=movies)
    return preds_df



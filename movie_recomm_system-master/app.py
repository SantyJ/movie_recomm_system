# OPTION A: EXPLAINABILITY — Natural Language Explanation Generator
# =============================================================================

def generate_explanation(user_history, rec_movie_id, movies_df):
    """
    Generates a human-readable explanation for why a movie was recommended.

    Implements post-hoc content-based explanation on top of SVD predictions:
    SVD itself uses only collaborative signals (ratings), but the explanation
    borrows genre metadata to make the recommendation interpretable to the user.

    Strategy:
      1. Find the recommended movie's genres
      2. Look at movies the user rated >= 4.0 (their "liked" history)
      3. Find the liked movie with the most genre overlap with the recommendation
      4. Generate a templated natural language sentence referencing that movie

    Args:
        user_history  (DataFrame): This user's training ratings.
        rec_movie_id  (int)      : movieId of the recommended movie.
        movies_df     (DataFrame): Full movie metadata (title, genres).

    Returns:
        str: A natural language explanation string.
    """
    # Look up the recommended movie's genre list
    rec_movie_row = movies_df[movies_df['movieId'] == rec_movie_id]
    if rec_movie_row.empty:
        return "Because it is globally popular."

    rec_genres = set(rec_movie_row.iloc[0]['genres'].split('|'))

    # Only consider movies the user genuinely liked (rated 4.0 or above)
    high_rated_history = user_history[user_history['rating'] >= 4.0]
    if high_rated_history.empty:
        return "Based on your general viewing patterns."

    # Merge with metadata to get genre info for each liked movie
    hist_movies = pd.merge(high_rated_history, movies_df, on='movieId')

    # Find the liked movie with maximum genre overlap with the recommendation
    best_match  = None
    max_overlap = 0

    for _, row in hist_movies.iterrows():
        hist_genres = set(row['genres'].split('|'))
        overlap     = len(rec_genres.intersection(hist_genres))
        if overlap > max_overlap:
            max_overlap = overlap
            best_match  = row['title']

    if best_match and max_overlap > 0:
        # Randomly pick from multiple explanation templates to avoid
        # every card showing identical wording
        templates = [
            f"We recommend this because you highly rated structurally similar movies like **{best_match}**.",
            f"Because you enjoyed **{best_match}**, our AI found strong thematic overlaps here.",
            f"This shares similar genre DNA with **{best_match}**, which you previously rated highly.",
            f"Fans of **{best_match}** frequently cluster into this recommendation space."
        ]
        return random.choice(templates)
    else:
        # Fallback when no genre overlap is found — the SVD latent factors
        # found collaborative similarity even without obvious genre overlap
        return "Our Model-Based SVD AI found latent collaborative similarities with your profile!"


